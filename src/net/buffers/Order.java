package net.buffers;

/**
 * The enum Order.
 */
public enum Order {
    /**
     * Big endian order.
     */
    BIG_ENDIAN,
    /**
     * Little endian order.
     */
    LITTLE_ENDIAN,
    /**
     * Big middle endian order.
     */
    BIG_MIDDLE_ENDIAN,
    /**
     * Little middle endian order.
     */
    LITTLE_MIDDLE_ENDIAN
}